"""CLI for the offline cost/latency harness — ``python -m llmbench``.

Prints the measurement report (markdown by default, ``--json`` for machine-readable
output). Deterministic in ``(--seed, --trials)``, so the numbers in
``docs/LLM_COST_LATENCY.md`` are regenerated by running this with the defaults.
"""

from __future__ import annotations

import argparse
import json
import sys
from typing import Sequence

from .harness import DEFAULT_SEED, DEFAULT_TRIALS, measure, render_report


def main(argv: Sequence[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        prog="python -m llmbench", description=__doc__
    )
    parser.add_argument(
        "--trials",
        type=int,
        default=DEFAULT_TRIALS,
        help=f"jittered latency samples per prompt (default {DEFAULT_TRIALS})",
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=DEFAULT_SEED,
        help=f"run seed; fixes the latency jitter (default {DEFAULT_SEED})",
    )
    parser.add_argument(
        "--json",
        action="store_true",
        help="emit the report as JSON instead of markdown",
    )
    args = parser.parse_args(argv)

    report = measure(trials=args.trials, seed=args.seed)

    if args.json:
        sys.stdout.write(json.dumps(report.to_dict(), indent=2, sort_keys=True) + "\n")
    else:
        sys.stdout.write(render_report(report) + "\n")
    return 0


if __name__ == "__main__":  # pragma: no cover - thin CLI wrapper
    raise SystemExit(main())
